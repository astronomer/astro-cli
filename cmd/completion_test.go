package cmd

import (
	"testing"

	testUtil "github.com/astronomer/astro-cli/pkg/testing"
	"github.com/stretchr/testify/assert"
)

func TestErrorCompletionRootCommand(t *testing.T) {
	testUtil.InitTestConfig()
	_, err := executeCommand("completion")
	assert.EqualError(t, err, "accepts 1 arg(s), received 0")
}

func TestBashCompletionRootCommand(t *testing.T) {
	testUtil.InitTestConfig()
	output, err := executeCommand("completion", "bash")
	assert.NoError(t, err)
	assert.Contains(t, output, "bash completion for astro")
}

func TestZshCompletionRootCommand(t *testing.T) {
	testUtil.InitTestConfig()
	// ignore long zsh script as output, it's just wrapper around bash completion
	_, err := executeCommand("completion", "zsh")
	assert.NoError(t, err)
}
